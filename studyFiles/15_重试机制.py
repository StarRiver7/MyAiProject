# 八、Agent 场景中的作用
# =====================================================
# 用户问题
# -> LLM
# -> Tool Call
# -> 再次 LLM
# -> Memory
# -> 最终输出
#
# 任意一个环节失败都可能导致整个链路中断
# 所以重试机制是 Agent 系统稳定性的核心


# =====================================================
# 九、面试总结
# =====================================================
# 大模型重试机制主要用于处理临时性失败，
# 常见场景包括限流、网络超时和服务端异常。
# 通常采用“指数退避 + 随机抖动”的方式，
# 避免请求风暴和惊群效应。
# 对于 400、401 等确定性错误不应重试。
# 在 Agent 系统中，重试机制是保障稳定性的关键能力。
