# =====================================================
# 一、什么是重试机制（Retry Mechanism）
# =====================================================
# 当调用大模型 API 失败时，系统不会立即报错退出，
# 而是按照一定策略再次发起请求。
#
# 核心目的：
# 1. 提高系统稳定性
# 2. 应对临时性错误
# 3. 避免单次失败导致整个业务链路中断


# =====================================================
# 二、为什么需要重试
# =====================================================
# 1）限流错误
# 状态码：429
# 含义：请求过多，服务器暂时拒绝服务
# 场景：高并发、多用户同时访问、Agent 多轮调用
# 可重试：是

# 2）网络异常
# 例如：timeout / connection reset / read timeout
# 含义：网络临时波动
# 可重试：是

# 3）服务端异常
# 状态码：500 / 502 / 503 / 504
# 含义：服务端临时故障
# 可重试：是


# =====================================================
# 三、哪些错误不能重试
# =====================================================
# 400 Bad Request   -> 请求参数错误
# 401 Unauthorized -> API Key 错误
# 403 Forbidden    -> 权限不足
#
# 原则：
# 只重试“临时性错误”
# 不重试“确定性错误”


# =====================================================
# 四、核心重试策略：指数退避
# =====================================================
# 第1次失败：1秒
# 第2次失败：2秒
# 第3次失败：4秒
# 第4次失败：8秒
#
# 公式：
# delay = base * 2 ** retry_count


# =====================================================
# 五、为什么要加随机抖动（Jitter）
# =====================================================
# 防止多个请求同时在同一时间重试，造成“惊群效应”
#
# 公式：
# delay = 2 ** retry + random.uniform(0, 1)


# =====================================================
# 六、基础代码实现
# =====================================================
import time
import random


def call_llm_with_retry(func, max_retry=3):
    """
    基础版重试机制
    参数:
        func: 实际调用大模型的方法
        max_retry: 最大重试次数
    """
    for i in range(max_retry):
        try:
            return func()
        except Exception as e:
            wait = (2 ** i) + random.uniform(0, 1)
            print(f"第{i + 1}次失败: {e}")
            print(f"等待 {wait:.2f} 秒后重试...")
            time.sleep(wait)

    raise Exception("超过最大重试次数，调用失败")


